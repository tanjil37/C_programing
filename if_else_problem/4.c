#include <stdio.h>
int main()
{
    int num;
    scanf("%d", &num);
    if (num == 2)
    {
        printf("It is a prime number");
    }
    else if ("num > 2")
    {
        if (num % 2 != 0)
        {
            printf("It is a prime number");
        }
    }
    else
    {
        printf("It is not a prime number");
    }
    return 0;
}